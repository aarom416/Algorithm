n = int(input())
res=''
if n==0:
    print(0)
    exit()
while n: #진수 계산법 사용 ->나머지가 +1 인 부분 활용
    if n%-2==0:
        n=n//-2
        res='0'+res
    else:
        n=(n//-2)+1
        res='1'+res
print(res)
